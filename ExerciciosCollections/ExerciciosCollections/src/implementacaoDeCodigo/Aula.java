package implementacaoDeCodigo;

public class Aula {
	// Atributos
	private String titulo;
	private int tempo;
	
	// Métodos
	public Aula(String titulo, int tempo) {
	this.titulo = titulo;
	this.tempo = tempo;
	}
	// Get e Set
	public String getTitulo() {
	return titulo;
	}
	public int getTempo() {
	return tempo;

	}
}

